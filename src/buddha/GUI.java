    public void updateExposures(long e) {
        int prefix = 0;
        while(e>1000 && prefix < siPrefix.length()-1) {
            prefix++;
            e /= 1000;
        }
        exposuresLabel.setText(e+" "+siPrefix.charAt(prefix));
    }

