    void render() {
        Graphics2D g = (Graphics2D) bufStrat.getDrawGraphics();
        g.setTransform(AffineTransform.getScaleInstance(
                (double)canvas.getWidth()/Buddha.sizex,
                (double)canvas.getHeight()/Buddha.sizey));
        Buddha.renderer.render(g);
        bufStrat.show();
    }
    
